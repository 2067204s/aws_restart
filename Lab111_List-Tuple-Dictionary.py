#EXERCISE 1: INTRODUCING THE LIST DATA TYPE
myFruitlist = ["apple", "banana", "cherry"]  #Defining a list
print(myFruitlist)
print(type(myFruitlist))
print(myFruitlist[0])    #Accessing a list by position
print(myFruitlist[1])
print(myFruitlist[2])
myFruitlist[2]  = "orange"   # Changing the values in list
print(myFruitlist) 
